    private boolean checkValidation() {
        if (txtCashierUsername.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Cashier Username Cannot be Empty");
            return false;
        }
        if (pswCashierPassword.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Cashier Password Cannot be Empty");
            return false;
        }
        return true;
    }

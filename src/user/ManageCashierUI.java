    private void btnAddCashierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCashierActionPerformed
        // TODO add your handling code here:
        if(checkValidation()) {
            String cashierUsername = txtCashierUsername.getText();
            String cashierPassword = pswCashierPassword.getText();
        
            Cashier cashier = new Cashier(cashierUsername, cashierPassword);
        
//            String line = "CashierID : " + cashier.getCashierID() + "\n" +
//                        "Cashier Name : " + cashier.getCashierName() + "\n" + 
//                        "Cashier Username : " + cashier.getCashierUsername() + "\n" + 
//                        "Cashier Password : " + cashier.getCashierPassword();
        
            boolean result = cashierdb.add(cashier);
            if (result) {
                JOptionPane.showMessageDialog(this,"New Cashier is Added Successfully!!");
            } else {
                JOptionPane.showMessageDialog(this,"New Cashier isn't Added Successfully!!");
            }
            txtCashierUsername.setText("");
            pswCashierPassword.setText("");
        }   
    }//GEN-LAST:event_btnAddCashierActionPerformed

    private void btnViewallCashiersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewallCashiersActionPerformed
        // TODO add your handling code here:
        ArrayList<Cashier> cashierList = cashierdb.getAll();
        tblModel.setRowCount(0);
        for (Cashier cashier:cashierList) {
            String cashierUsername = cashier.getCashierUsername();
            String cashierPassword = cashier.getCashierPassword();
            tblModel.addRow(new Object[] {cashierUsername, cashierPassword });
        }
    }//GEN-LAST:event_btnViewallCashiersActionPerformed

    private void btnSearchCashierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchCashierActionPerformed
        // TODO add your handling code here:
        String cashierUsername = JOptionPane.showInputDialog("Enter the Cashier ID : ");
        Cashier cashier = cashierdb.get(cashierUsername);
        if (cashier != null) {
            txtCashierUsername.setText(cashier.getCashierUsername());
            pswCashierPassword.setText(cashier.getCashierPassword());
        } else {
            JOptionPane.showMessageDialog(this, "The Record is not Found!!");
        }
    }//GEN-LAST:event_btnSearchCashierActionPerformed

    private void btnDeleteCashierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteCashierActionPerformed
        // TODO add your handling code here:
        String cashierUsername = JOptionPane.showInputDialog("Enter the Cashier ID : ");
        boolean deleted = cashierdb.delete(cashierUsername);
        if (deleted){
            JOptionPane.showMessageDialog(this, "The Cashier is being Successfully Deleted!!");
        } else {
            JOptionPane.showMessageDialog(this, "The Record is not Found!!");
        }
    }//GEN-LAST:event_btnDeleteCashierActionPerformed


    private void btnAddProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProductActionPerformed
        // TODO add your handling code here:
        if(checkValidation()) {
            String  productID = txtProductID.getText();
            String categoryID = String.valueOf(cmbCategoty.getSelectedItem());
            String productName = txtProductName.getText();
            int productPrice = Integer.valueOf(txtProductPrice.getText());
            String ProductSerialNumber = txtProductSerialNumber.getText();
            int productQuantity = Integer.valueOf(txtProductQuantity.getText());
        
            Products products = new Products(productID, categoryID, productName, productPrice, ProductSerialNumber, productQuantity);
        
//            String line = "ProductID : " + product.getProductID() + "\n " 
//                      + "CategoryID : "  + product.getCategoryID() + "\n" + " product Name : " 
//                      + product.getProductName() + "\n" + "Product Price : " + 
//                      product.getProductPrice() + "\n"  + "Serial Number : " +
//                      product.getProductSerialNumber() + "\n" +"Quantity : " + 
//                      product.getProductQuantity();
        
            boolean result = productsdb.add(products);
            if (result) {
                JOptionPane.showMessageDialog(this,"New Product is Added Successfully!!");
            } else {
                JOptionPane.showMessageDialog(this,"New Product isn't Added Successfully!!");
            }
//          System.out.println(line);
            
            // Clear the text fields
            txtProductID.setText("");
            cmbCategoty.setSelectedItem("Mac");
            txtProductName.setText("");
            txtProductPrice.setText("");
            txtProductSerialNumber.setText("");
            txtProductQuantity.setText("");
        }
    }//GEN-LAST:event_btnAddProductActionPerformed

    private void btnViewallProductsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewallProductsActionPerformed
        // TODO add your handling code here:
        ArrayList<Products> productsList = productsdb.getAll();
            tblModel.setRowCount(0);
            for (Products products:productsList) {
                String  productID = products.getProductID();
                String categoryID = products.getCategoryID();
                String productName = products.getProductName();
                int productPrice = products.getProductPrice();
                String ProductSerialNumber = products.getProductSerialNumber();
                int productQuantity = products.getProductQuantity();
                tblModel.addRow(new Object[] {productID, categoryID, productName, productPrice , ProductSerialNumber , productQuantity});
            }
    }//GEN-LAST:event_btnViewallProductsActionPerformed

    private void btnSearchProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchProductActionPerformed
        // TODO add your handling code here:
        String productID = JOptionPane.showInputDialog("Enter the Product ID");
        Products products = productsdb.get(productID);
        if (products != null) {
            txtProductID.setText(String.valueOf(products.getProductID()));
            cmbCategoty.setSelectedItem(products.getCategoryID());
            txtProductName.setText(products.getProductName());
            txtProductPrice.setText(String.valueOf(products.getProductPrice()));
            txtProductSerialNumber.setText(products.getProductSerialNumber());
            txtProductQuantity.setText(String.valueOf(products.getProductQuantity()));
        } else {
            JOptionPane.showMessageDialog(this, "The Record is not Found!!");
        }
    }//GEN-LAST:event_btnSearchProductActionPerformed


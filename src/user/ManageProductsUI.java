    private boolean checkValidation() {
        if (txtProductID.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Product ID Cannot be Empty");
            return false;
        }
        if (txtProductName.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Product Name Cannot be Empty");
            return false;
        }
        if (txtProductPrice.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Product Price Cannot be Empty");
            return false;
        }
        try {
            int productPrice = Integer.valueOf(txtProductPrice.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Product Price Must Be Numeric!!");
            return false;
        }
        if (txtProductSerialNumber.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Serial Number of the Product Cannot be Empty");
            return false;
        }
        if (txtProductQuantity.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Product Quantity Cannot be Empty");
            return false;
        }
        try {
            int productQuantity = Integer.valueOf(txtProductQuantity.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Product Quantity Must Be Numeric!!");
            return false;
        }
        return true;
    }
    
